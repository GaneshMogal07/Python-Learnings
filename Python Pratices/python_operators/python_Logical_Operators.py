# Logical Operators Examples

a = True
b = False

# 1. and (Logical AND)
# Definition: Returns True if both conditions are True
print("1. a and b → " + str(a and b))  # False


# 2. or (Logical OR)
# Definition: Returns True if at least one condition is True
print("2. a or b → " + str(a or b))  # True


# 3. not (Logical NOT)
# Definition: Reverses the boolean value
print("3. not a → " + str(not a))  # False
print("4. not b → " + str(not b))  # True


