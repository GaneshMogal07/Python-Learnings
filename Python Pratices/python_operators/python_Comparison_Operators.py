# Comparison Operators Examples

a = 10
b = 5

# 1. == (Equal to)
# Definition: Returns True if both operands are equal
print("1. a == b → " + str(a == b))  # False  and 5==5 , output- true


# 2. != (Not equal to)
# Definition: Returns True if operands are not equal
print("2. a != b → " + str(a != b))  # True and 5 !=5 , false


# 3. > (Greater than)
# Definition: Returns True if left operand is greater than the right
print("3. a > b → " + str(a > b))  # True and 10< 5 , false


# 4. < (Less than)
# Definition: Returns True if left operand is less than the right
print("4. a < b → " + str(a < b))  # False and 10>5 , true


# 5. >= (Greater than or equal to)
# Definition: Returns True if left operand is greater than or equal to the right
print("5. a >= b → " + str(a >= b))  # True and 10 <= 5 ,false


# 6. <= (Less than or equal to)
# Definition: Returns True if left operand is less than or equal to the right
print("6. a <= b → " + str(a <= b))  # False and 10 >= 5 ,true.




