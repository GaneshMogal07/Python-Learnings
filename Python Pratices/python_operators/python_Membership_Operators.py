# Membership Operators Examples

my_list = [10, 20, 30, 40, 50]

# 1. in
# Definition: Returns True if a value exists in the sequence
print("1. 20 in my_list → " + str(20 in my_list))   # True
print("2. 60 in my_list → " + str(60 in my_list))   # False

# 2. not in
# Definition: Returns True if a value does NOT exist in the sequence
print("3. 60 not in my_list → " + str(60 not in my_list))  # True
print("4. 30 not in my_list → " + str(30 not in my_list))  # False


